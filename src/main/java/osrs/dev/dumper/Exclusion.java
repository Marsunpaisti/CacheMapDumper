    {
        this.id = id;
        this.tile = tile;
    }

    public static Boolean matches(int id)
    {
        for (Exclusion exclusion : VALUES)
        {
            if (exclusion.id == id)
            {
                return exclusion.tile;
            }
        }
        return null;
    }
